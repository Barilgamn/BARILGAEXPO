type ContextProps = {
  lang: Language;
  setLang: (l: Language) => void;
  t: (key: keyof typeof translations['mn']) => string;
};

const LanguageContext = createContext<ContextProps | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  const [lang, setLang] = useState<Language>('mn');

  const t = (key: keyof typeof translations['mn']) => {
    return translations[lang][key] || translations['mn'][key] || key;
  };

  return (
    <LanguageContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useTranslation = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useTranslation must be used within a LanguageProvider');
  }
  return context;
};
